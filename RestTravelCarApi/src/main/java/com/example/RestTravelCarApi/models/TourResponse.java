package com.example.RestTravelCarApi.models;

import java.util.List;

public class TourResponse {
    private List<CategoryTourDTO> categoryTours;
    private List<ThemeTourDTO> themeTours;
    private List<DepartureDateDTO> departureDates;

    // Constructors
    public TourResponse(List<CategoryTourDTO> categoryTours, List<ThemeTourDTO> themeTours, List<DepartureDateDTO> departureDates) {
        this.categoryTours = categoryTours;
        this.themeTours = themeTours;
        this.departureDates = departureDates;
    }

    // Getters and setters
    public List<CategoryTourDTO> getCategoryTours() {
        return categoryTours;
    }

    public void setCategoryTours(List<CategoryTourDTO> categoryTours) {
        this.categoryTours = categoryTours;
    }

    public List<ThemeTourDTO> getThemeTours() {
        return themeTours;
    }

    public void setThemeTours(List<ThemeTourDTO> themeTours) {
        this.themeTours = themeTours;
    }

    public List<DepartureDateDTO> getDepartureDates() {
        return departureDates;
    }

    public void setDepartureDates(List<DepartureDateDTO> departureDates) {
        this.departureDates = departureDates;
    }
}
